
package SymbolTable;

public class FieldSymbol{
    
    private String type;
    private String name;

    public String getType(){
        return type;
    }


    public void setType(String t){
        type = t;
    }


    public String getName(){
        return name;
    }


    public void setName(String n){
        name = n;
    }


}
